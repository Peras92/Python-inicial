mood = input("Olá! Como te sentes hoje? ")

if mood == "feliz":
    print("É bom ver-te feliz!")
elif mood == "nervoso":
    print("Respira fundo 3 vezes, vais ver que consegues")
elif mood == "triste":
    print("Vais ver que as coisas vão melhorar, força!")
elif mood == "excitado":
    print("Optímo! Vamos dar uso a essa energia!")
elif mood == "relaxado":
    print("Boa, aproveita para descansar um pouco")
else:
    print("Não estou a reconhecer esse estado")

